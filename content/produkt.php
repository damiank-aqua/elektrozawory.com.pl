<p>Od ponad 30 lat zajmujemy się sprzedażą zaworów elektromagnetycznych o różnym przeznaczeniu przemysłowym. W
ofercie znajdują się elektrozawory szeroko wykorzystywane w automatyce przemysłowej do kontrolowania przepływu
cieczy, gazów oraz mediów agresywnych.</p>
<strong>Dostępne są w dwóch kategoriach:</strong>
<ul>
<li>- w funkcji Normalnie Zamkniętej – w przypadku braku zasilania cewki medium nie zostaje przepuszczone.</li>
<li>- w funkcji Normalnie Otwartej – w przypadku braku zasilania cewki medium jest swobodnie przepuszczane.</li>
<p>Nie jest to jednak jedyny podział: dla ułatwienia przygotowaliśmy wyszukiwarkę, dzięki której można odnaleźć zawór
elektromagnetyczny odpowiadający określonym potrzebom przemysłowym.</p>
<strong>Poszczególne produkty można podzielić ze względu na takie parametry, jak:</strong>
<ul>
<li>- wspomniana już funkcja działania,</li>
<li>- średnica przyłącza,</li>
<li>- rodzaj obsługiwanego medium (woda, para wodna, powietrze, olej, glikol, media agresywne),</li>
<li>- rodzaj uszczelnienia,</li>
<li>- zasadę działania (pośredniego działania i bezpośredniego/kombinowanego działania),</li>
<li>- materiał wykonania korpus (mosiądz, stal nierdzewna/kwasoodporna, plastik).</li>
</ul><br>
<p>Zachęcamy do skorzystania z wygodnej wyszukiwarki, a w razie dodatkowych pytań – kontaktu z naszymi specjalistami,
którzy doradzą wybór odpowiedniego rozwiązania.</p>